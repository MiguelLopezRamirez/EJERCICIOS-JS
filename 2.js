// Operaciones matemáticas:
// Declara dos variables con números y realiza las operaciones básicas: suma, resta, multiplicación y división. Imprime los resultados.

let x = 3;
let y = 4;

//Suma
console.log("Suma de ", x, " y ", y, " = ", x + y)

//Resta
console.log("Resta de ", x, " y ", y, " = ", x - y)

//multiplicación
console.log("Multiplicación de ", x, " y ", y, " = ", x * y)

//División
console.log("División de ", x, " y ", y, " = ", x / y)