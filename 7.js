// Recorriendo arreglos con forEach:
// Dado un arreglo let numeros = [2, 4, 6, 8, 10], usa forEach para imprimir cada nÃºmero en la consola.
let numeros = [2, 4, 6, 8, 10]; //Array de numeros

numeros.forEach(numero => { //Por each de array para recorrer todos los elemenos como "numero"
    console.log(numero) //Se imprime cada "numero" del array
});